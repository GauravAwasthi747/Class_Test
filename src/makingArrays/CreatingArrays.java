package makingArrays;

public class CreatingArrays {
    public static void main(String[] args) {
        int[] myArrays = new int[5];

        myArrays[0] = 34;
        myArrays[1] = 23;
        myArrays[2] = 44;
        myArrays[3] = 56;
        myArrays[4] = 78;

        System.out.println(myArrays[0]);
        System.out.println(myArrays[1]);
        System.out.println(myArrays[2]);
        System.out.println(myArrays[3]);
        System.out.println(myArrays[4]);
    }
}
